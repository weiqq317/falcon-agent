package funcs

import (
	"falcon-agent/g"

	"github.com/open-falcon/falcon-plus/common/model"
)

type FuncsAndInterval struct {
	Fs       []func() []*model.MetricValue
	Interval int
}

var Mappers []FuncsAndInterval

func BuildMappers() {
	interval := g.Config().Transfer.Interval
	Mappers = []FuncsAndInterval{
		{
			Fs: []func() []*model.MetricValue{
				AgentMetrics,
				CpuMetrics,
				MemMetrics,
				DiskMetrics,
				NetMetrics,
			},
			Interval: interval,
		},
		{
			Fs: []func() []*model.MetricValue{
				ProcMetrics,
				SnmpMetrics,
			},
			Interval: interval,
		},
	}
}
